import { MAX_RESOURCE_FILE_SIZE } from '@/config/constants';

export function isResourceFileWithinSizeLimit(file: File): boolean {
  return file.size <= MAX_RESOURCE_FILE_SIZE;
}

export function formatResourceFileSize(bytes: number | null): string {
  if (!bytes) return '';
  if (bytes < 1024) return `${bytes} B`;
  if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(1)} KB`;
  if (bytes < 1024 * 1024 * 1024) return `${(bytes / (1024 * 1024)).toFixed(1)} MB`;
  return `${(bytes / (1024 * 1024 * 1024)).toFixed(2)} GB`;
}
